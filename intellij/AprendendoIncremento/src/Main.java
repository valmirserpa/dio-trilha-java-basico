public class Main {
    public static void main(String[] args) {

        // PÓS-INCREMENTO

        int a = 10;
        int b = a++;

        System.out.println(b);
        System.out.println(a);

        System.out.println();

        int x = 5;
        System.out.println(x++);
        System.out.println(x);

        System.out.println();


        // PRÉ- INCREMENTO
        int y = 5;
        System.out.println(++y);
        System.out.println(y);

        System.out.println();

        int c = 5;
        int d = ++c;

        System.out.println(c);
        System.out.println(d);

    }
}