public class OperadorTernario {
    public static void main(String[] args) {
        

        int a, b;

        a=5;
        b=6;

        String resultado = "";

        if (a==b) {
            resultado="verdadeiro";
        } else {
            resultado="falso";
        }

        System.out.println(resultado);


        int c, d;

        c=5;
        d=5;

        String resultado1 = "";

        if (c==d) {
            resultado1="verdadeiro";
        } else {
            resultado1="falso";
        }

        System.out.println(resultado1);












    }
}
