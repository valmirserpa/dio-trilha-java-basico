public class Incremento {
    public static void main(String[] args) {
        
        int numero = 5;
 
        System.out.println(++ numero);
        System.out.println(-- numero);
        
        System.out.println(numero ++);
        System.out.println(numero --);


        int a = 5;
        int b = ++ a;
        System.out.println(b);

    


    }
}
